# bool type - Either True or False
bool_type_true = True
print("bool_type_true: ", type(bool_type_true))

# bool type - Either True or False
bool_type_false = False
print("bool_type_false: ", type(bool_type_false))

# int type -
int_type = 47
print("int_type: ", type(int_type))

# float type
float_type = 3.14
print("float_type: ", type(float_type))

# Complex type
complex_type = 3j, 5 + 9j
print("complex_type: ", type(complex_type))

# str type
str_type = "testing"
print("str type: ", type(str_type))

# list type
list_type = ["Pizza", "Burgers", "Nachos"]
print("list_type: ", type(list_type))
