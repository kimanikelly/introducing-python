# Booleans - Either True or False
bool_type_true = True
print("bool_type_true:", type(bool_type_true))

bool_type_false = False
print("bool_type_false:", type(bool_type_false))

# Integers - Whole numbers
int_type = 777
print("int_type:", type(int_type))

# Floats - Numbers with decimal points
float_type = 3.14
print("float_type:", type(float_type))
