# Example of floating point division
float_example = 200
float_example /= 3
print(float_example)

# Example of integer division
truncated_example = 13
truncated_example //= 4
print(truncated_example)

# The %(modulo) sign is used to produce the remainder when the first number is divided by the second
modulo_example = 9 % 5
print(modulo_example)

# Example of truncated quotient and remainder
truncated_and_remainder = divmod(9, 5)
print(truncated_and_remainder)

# Example of exponentation
expo_1 = 2**3
print(expo_1)

expo_2 = 2.0 ** 3
print(expo_2)

expo_3 = 2 ** 3.0
print(expo_3)

expo_4 = 0 ** 3
print(expo_4)
