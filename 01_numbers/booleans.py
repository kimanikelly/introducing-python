# The built-in function bool() can convert any data type to a boolean

# Nonzero numbers are considered True
num_one = 24
print(bool(num_one))

num_two = 48
print(bool(num_two))

num_three = 72
print(bool(num_three))

# Zero valued numbers are considered False
num_four = 0
print(bool(num_four))

num_five = 0.0
print(bool(num_five))
