# Addition
add = 5 + 9
print(add)

# Subtraction
subtract = 100 - 50
print(subtract)

# Multiplication
multiply = 6 * 6
print(multiply)

# Two types of Division is provided
# Floating point(Decimal) division is used with the / sign
# Integer (truncating) division is used with the // sign

# Diving an integer with integer will return a float
decimal_division = 9 / 5
print(decimal_division)

# Truncating will remove any remainder
integer_division = 9 // 5
print(integer_division)

# Any division by zero with throw an error
